bool COMP(pair<int, int>& p1, pair<int, int>& p2) {
  return p1.first < p2.first;
}



void solve() {
  int n;
  cin >> n;
  ve<pair<int, int>> a;
  for(int i = 0; i < n; i++) {
    int l, r; cin >> l >> r;
    a.pb({l, r});
  }

  sort(a.begin(), a.end(), COMP);
  int res1 = 0, res2 = 0;
  ve<pair<int, int>> sta;
  for(pair<int, int>& p : a) {
    if(sta.size() == 0) sta.pb(p);
    else {
      if(p.first > sta[sta.size() - 1].second) {
        sta.pb(p);
      } else {
        sta[sta.size() - 1].second = max(sta[sta.size() - 1].second, p.second);
      }
    }
  }

  for(pair<int, int>& p : sta) {
    res1 = max(res1, p.second - p.first);
  }
  
  for(int i = 1; i < sta.size(); i++) {
      res2 = max(res2, sta[i].first - sta[i - 1].second);
  }

  cout << res1 << " " << res2 << endl;
}

